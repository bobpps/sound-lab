import { screen, waitFor } from "@testing-library/react";
import userEvent from "@testing-library/user-event";
import { afterEach, beforeEach, describe, expect, it, vi } from "vitest";
import { renderWithProviders } from "../../../test-utils.tsx";
import { LlmActionDialog } from "./LlmActionDialog.tsx";

const llmProviders = [
  {
    id: "openai",
    name: "OpenAI",
    type: "llm" as const,
    enabled: true,
    created_at: "2026-04-07T08:00:00.000Z",
  },
];

const generatedDialog = {
  id: 9,
  title: "Generated by LLM",
  description: null,
  language: "fr-FR",
  created_by: null,
  created_at: "2026-04-07T08:10:00.000Z",
  messages: [
    {
      id: 101,
      dialog_id: 9,
      order: 1,
      character: 1,
      text: "Bonjour",
    },
  ],
};

const editedDialog = {
  id: 5,
  title: "Greeting practice",
  description: null,
  language: "en-US",
  created_by: null,
  created_at: "2026-04-07T08:00:00.000Z",
  messages: [
    {
      id: 55,
      dialog_id: 5,
      order: 1,
      character: 1,
      text: "A rewritten greeting",
    },
  ],
};

function jsonResponse(body: unknown, status = 200) {
  return new Response(JSON.stringify(body), {
    status,
    headers: {
      "Content-Type": "application/json",
    },
  });
}

function extractUrl(input: string | URL | Request): string {
  if (typeof input === "string") {
    return input;
  }

  if (input instanceof URL) {
    return input.pathname;
  }

  return input.url;
}

describe("LlmActionDialog", () => {
  beforeEach(() => {
    vi.stubGlobal(
      "fetch",
      vi.fn(async (input: string | URL | Request, init?: RequestInit) => {
        const url = extractUrl(input);
        const method = init?.method ?? "GET";

        if (url.endsWith("/api/providers?type=llm") && method === "GET") {
          return jsonResponse(llmProviders);
        }

        if (url.endsWith("/api/llm/openai/models") && method === "GET") {
          return jsonResponse(["gpt-4o"]);
        }

        if (url.endsWith("/api/services/generate-dialog") && method === "POST") {
          return jsonResponse(generatedDialog, 201);
        }

        if (url.endsWith("/api/services/edit-dialog") && method === "POST") {
          return jsonResponse(editedDialog);
        }

        return jsonResponse(
          {
            statusCode: 404,
            error: "Not Found",
            message: "Not Found",
          },
          404,
        );
      }),
    );
  });

  afterEach(() => {
    vi.unstubAllGlobals();
  });

  it("submits generate mode with provider, model, language, message count, and prompt", async () => {
    const user = userEvent.setup();
    const onSuccess = vi.fn();

    renderWithProviders(
      <LlmActionDialog
        mode="generate"
        initialLanguage="fr-FR"
        initialMessageCount={4}
        onClose={() => {}}
        onSuccess={onSuccess}
      />,
    );

    await screen.findByRole("dialog", { name: "Generate Dialog" });
    await screen.findByRole("option", { name: "gpt-4o" });

    await user.type(
      screen.getByLabelText("Prompt"),
      "Generate a short bilingual welcome exchange",
    );
    await user.click(screen.getByRole("button", { name: "Generate Dialog" }));

    await waitFor(() => {
      expect(onSuccess).toHaveBeenCalledWith(generatedDialog);
    });

    expect(fetch).toHaveBeenCalledWith(
      "/api/services/generate-dialog",
      expect.objectContaining({
        method: "POST",
        body: JSON.stringify({
          providerId: "openai",
          model: "gpt-4o",
          language: "fr-FR",
          prompt: "Generate a short bilingual welcome exchange",
          messageCount: 4,
        }),
      }),
    );
  });

  it("submits edit mode with dialog id and instructions", async () => {
    const user = userEvent.setup();
    const onSuccess = vi.fn();

    renderWithProviders(
      <LlmActionDialog
        mode="edit"
        dialogId={5}
        onClose={() => {}}
        onSuccess={onSuccess}
      />,
    );

    await screen.findByRole("dialog", { name: "Edit Dialog with LLM" });
    await screen.findByRole("option", { name: "gpt-4o" });

    expect(screen.queryByLabelText("Language")).not.toBeInTheDocument();
    expect(screen.queryByLabelText("Message count")).not.toBeInTheDocument();

    await user.type(
      screen.getByLabelText("Instructions"),
      "Make the first line warmer and more polished",
    );
    await user.click(screen.getByRole("button", { name: "Apply Edit" }));

    await waitFor(() => {
      expect(onSuccess).toHaveBeenCalledWith(editedDialog);
    });

    expect(fetch).toHaveBeenCalledWith(
      "/api/services/edit-dialog",
      expect.objectContaining({
        method: "POST",
        body: JSON.stringify({
          dialogId: 5,
          providerId: "openai",
          model: "gpt-4o",
          instructions: "Make the first line warmer and more polished",
        }),
      }),
    );
  });
});
